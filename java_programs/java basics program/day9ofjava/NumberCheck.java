class NumberCheck {
    public static void main(String[] args) {
        // Hardcoded numbers
        int num1 = 57;
        int num2 = -78;
        int num3 = 0;

        // Check num1
        if (num1 > 0) {
            System.out.println(num1 + " is a positive number");
        } else if (num1 < 0) {
            System.out.println(num1 + " is a negative number");
        } else {
            System.out.println(num1 + " is neutral");
        }

        // Check num2
        if (num2 > 0) {
            System.out.println(num2 + " is a positive number");
        } else if (num2 < 0) {
            System.out.println(num2 + " is a negative number");
        } else {
            System.out.println(num2 + " is neutral");
        }

        // Check num3
        if (num3 > 0) {
            System.out.println(num3 + " is a positive number");
        } else if (num3 < 0) {
            System.out.println(num3 + " is a negative number");
        } else {
            System.out.println(num3 + " is neutral");
        }
    }
}

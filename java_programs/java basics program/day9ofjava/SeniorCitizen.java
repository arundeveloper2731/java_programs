class SeniorCitizen {
    public static void main(String[] args) {
        // Hardcoded ages
        int age1 = 65;
        int age2 = 45;

        // Check age1
        if (age1 >= 60) {
            System.out.println(age1 + " - Senior Citizen");
        } else {
            System.out.println(age1 + " - Not a Senior Citizen");
        }

        // Check age2
        if (age2 >= 60) {
            System.out.println(age2 + " - Senior Citizen");
        } else {
            System.out.println(age2 + " - Not a Senior Citizen");
        }
    }
}
